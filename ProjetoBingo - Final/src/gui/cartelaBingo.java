    //altera a cor quando o botao for clicado
    private void marcaNumero(JButton btn) {
        Integer num = Integer.valueOf(btn.getText());
        if (!this.numerosMarcados.contains(num)) {
            this.numerosMarcados.add(num);
            btn.setBackground(Color.DARK_GRAY);
            btn.setForeground(Color.WHITE);
        } else {
            this.numerosMarcados.remove(num);
            btn.setBackground(Color.LIGHT_GRAY);
            btn.setForeground(Color.BLACK);
        }

    }

