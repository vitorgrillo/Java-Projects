    private HashMap<String, Integer> geraNumeros() {
        List<Integer> numerosSorteados = new ArrayList<Integer>();
        HashMap<String, Integer> map = new HashMap<String, Integer>();

        Random r = new Random();

        while (numerosSorteados.size() <= 24) {
            int numeroRandom = r.nextInt(30) + 1;
            if (!numerosSorteados.contains(numeroRandom)) {
                numerosSorteados.add(numeroRandom);
            }
        }

        Collections.sort(numerosSorteados);

        for (int i = 1; i <= 24; i++) {
            map.put("btn" + i, numerosSorteados.get(i - 1));
        }

        return map;
    }

