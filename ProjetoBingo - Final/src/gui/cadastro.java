    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        // instanciando a classe Usuario do pacote modelo e criando seu objeto usuarios
        Usuario usuarios = new Usuario();
        EmailValidator email = new EmailValidator();
        usuarios.setEmail(txtEmail.getText());
        usuarios.setSenha(txtSenha.getText());
        usuarios.setSenha(txtConfirmar.getText());
        
        
        //criando variavel que vai receber a senha digitada
        String senha_digitada = null;

        //entrando com senha
        senha_digitada = txtSenha.getText();  

        //verificando se senha digitada é igual à senha válida, definida anteriormente
        //sempre que fizer comparação entre Strings, utilize o 'equals()', se quiser saber o porque procure no forum, tem explicações boas sobre isso
        //no seu código vc utilizou um 'while' que é uma estrutura de repetição, nesse caso não é preciso repetição, e sim uma estrutura de controle, que é o caso do 'if'
        if (senha_digitada.equals(txtConfirmar.getText())) {
            //senha certa
           // faz a validação dos dados
           if(email.validate(txtEmail.getText()) == false){
               JOptionPane.showMessageDialog(null, "Email digitado é inválido !");
           }          
           else if ((txtNome.getText().isEmpty() || txtEmail.getText().isEmpty()) || (txtSenha.getText().isEmpty()) || (txtConfirmar.getText().isEmpty())) {
               JOptionPane.showMessageDialog(null, "Preencha todos os campos. ");
           }
           else {
                // instanciando a classe UsuarioDAO do pacote dao e criando seu objeto dao
                UsuarioDAO dao = new UsuarioDAO();
                dao.adiciona(usuarios);
                JOptionPane.showMessageDialog(null, "Usuário "+txtEmail.getText()+" inserido com sucesso! ");
           }
        } else {
            //senha errada
            JOptionPane.showMessageDialog(null, "Os campos 'Senha' e Confirma Senha são diferentes.");
        }        

        // apaga os dados preenchidos nos campos de texto
        txtNome.setText("");
        txtEmail.setText("");
        txtSenha.setText("");
        txtConfirmar.setText("");
    }//GEN-LAST:event_btnSalvarActionPerformed

