    private void jButtonCadUsSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadUsSalvarActionPerformed
        if (jTextFieldNomeUsuario.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha o nome do usuário");
            jTextFieldNomeUsuario.requestFocus();
        } else if (jTextFieldLoginUsuario.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha o login do usuário");
            jTextFieldLoginUsuario.requestFocus();
        } else if (jPasswordFieldSenhaUsuario.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha a senha do usuário");
            jTextFieldLoginUsuario.requestFocus();
        } else if (jPasswordFieldConfSenhaUsuario.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Confirme a senha do usuário");
            jTextFieldLoginUsuario.requestFocus();
        } else if (flag == 1) {
            mod.setNomeUsuario(jTextFieldNomeUsuario.getText());
            mod.setLoginUsuario(jTextFieldLoginUsuario.getText());
            mod.setSenhaUsuario(jPasswordFieldSenhaUsuario.getText());
            mod.setTipoUsuario((String) jComboBoxTipoUsuario.getSelectedItem());
            dao.Salvar(mod);
            jTextFieldNomeUsuario.setText("");
            jTextFieldLoginUsuario.setText("");
            jPasswordFieldSenhaUsuario.setText("");
            jPasswordFieldConfSenhaUsuario.setText("");
            jTextFieldNomeUsuario.setEnabled(false);
            jTextFieldLoginUsuario.setEnabled(false);
            jPasswordFieldSenhaUsuario.setEnabled(false);
            jPasswordFieldConfSenhaUsuario.setEnabled(false);
            jComboBoxTipoUsuario.setEnabled(false);
            jButtonCadUsSalvar.setEnabled(false);
        } else {
            
            mod.setNomeUsuario(jTextFieldNomeUsuario.getText());
            mod.setLoginUsuario(jTextFieldLoginUsuario.getText());
            mod.setSenhaUsuario(jPasswordFieldSenhaUsuario.getText());
            mod.setTipoUsuario((String) jComboBoxTipoUsuario.getSelectedItem());
            dao.Alterar(mod);
            jTextFieldNomeUsuario.setText("");
            jTextFieldLoginUsuario.setText("");
            jPasswordFieldSenhaUsuario.setText("");
            jPasswordFieldConfSenhaUsuario.setText("");
            jTextFieldNomeUsuario.setEnabled(false);
            jTextFieldLoginUsuario.setEnabled(false);
            jPasswordFieldSenhaUsuario.setEnabled(false);
            jPasswordFieldConfSenhaUsuario.setEnabled(false);
            jComboBoxTipoUsuario.setEnabled(false);
            jButtonCadUsSalvar.setEnabled(false);
        }
    }//GEN-LAST:event_jButtonCadUsSalvarActionPerformed

