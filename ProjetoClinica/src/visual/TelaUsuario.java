    public void preencherTabela(String Sql) {
        ArrayList dados = new ArrayList();
        String [] colunas = new String []{"ID", "Nome", "Login", "Tipo"};
        conex.conexao();
        conex.executaSql(Sql);

        try {
            conex.rs.first();
            do {
                dados.add(new Object[]{conex.rs.getInt("cod_usuario"), conex.rs.getString("nome_usuario"), conex.rs.getString("login_usuario"), conex.rs.getString("tipo_usuario")});
            } while (conex.rs.next());

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Usuário não encontrado");
        }

        ModeloTabela modelo = new ModeloTabela(dados, colunas);

        jTableCadUsPesquisa.setModel(modelo);
        jTableCadUsPesquisa.getColumnModel().getColumn(0).setPreferredWidth(43);
        jTableCadUsPesquisa.getColumnModel().getColumn(0).setResizable(false);
        jTableCadUsPesquisa.getColumnModel().getColumn(1).setPreferredWidth(190);
        jTableCadUsPesquisa.getColumnModel().getColumn(1).setResizable(false);
        jTableCadUsPesquisa.getColumnModel().getColumn(2).setPreferredWidth(70);
        jTableCadUsPesquisa.getColumnModel().getColumn(2).setResizable(false);
        jTableCadUsPesquisa.getColumnModel().getColumn(3).setPreferredWidth(110);
        jTableCadUsPesquisa.getColumnModel().getColumn(3).setResizable(false);
        jTableCadUsPesquisa.getColumnModel().getColumn(4).setPreferredWidth(130);
        jTableCadUsPesquisa.getColumnModel().getColumn(4).setResizable(false);
        jTableCadUsPesquisa.getTableHeader().setReorderingAllowed(false);
        jTableCadUsPesquisa.setAutoResizeMode(jTableCadUsPesquisa.AUTO_RESIZE_OFF);
        jTableCadUsPesquisa.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        conex.desconectar();

    }
